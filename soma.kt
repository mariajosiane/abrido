fun soma(x: Int, y: Int): Int {                                          
    return x + y
}
fun subtracao(x: Int, y: Int): Int {                                          
    return x - y  
}

fun main(){
    println("Soma de dois números");                                                              
    println(soma(4,3))   
    println("subtracao de dois números")                                                                                                               
    println(subtracao(4,0))  
}

